package coldapp.giladpinker.technimilon;

public class Synonym {

    private String engSyn, hebSyn;

    public Synonym(String engSyn, String hebSyn) {
        this.engSyn = engSyn;
        this.hebSyn = hebSyn;
    }

    public String getEngSyn() {
        return engSyn;
    }

    public String getHebSyn() {
        return hebSyn;
    }
}
